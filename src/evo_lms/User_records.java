    private void searchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchKeyReleased
        try {
            String userinput = search.getText();
            conn =  EvoDatabase.connect();
            String getdata = "SELECT User_ID,User_FirstName,User_LastName,UserName,Password FROM users WHERE User_ID LIKE '%"+userinput+"%' OR User_FirstName LIKE '%"+userinput+"%' OR User_LastName LIKE '%"+userinput+"%' OR UserName LIKE '%"+userinput+"%'";
            pst = (PreparedStatement)conn.prepareStatement(getdata);
            ResultSet search_rs = pst.executeQuery();
            user_table.setModel(DbUtils.resultSetToTableModel(search_rs));

        } catch (SQLException ex) {
            Logger.getLogger(User_records.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_searchKeyReleased

    private void user_tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_user_tableMouseClicked
        int rowindex = user_table.getSelectedRow();
        selectedid = (int) user_table.getModel().getValueAt(rowindex, 0);
        
        try {
            String getrowdata = "SELECT * FROM users WHERE User_ID="+selectedid;
            pst = (PreparedStatement)conn.prepareStatement(getrowdata);
            rs = pst.executeQuery();
            
            if(rs.next()==true){
                userfirstname.setText(rs.getString("User_FirstName"));
                userlastname.setText(rs.getString("User_LastName"));
                userusername.setText(rs.getString("UserName"));
                userpassword.setText(rs.getString("Password"));
                
            }
        
        } catch (SQLException ex) {
            Logger.getLogger(Student_records.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }//GEN-LAST:event_user_tableMouseClicked


