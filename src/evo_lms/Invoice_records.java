    private void invoice_tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_invoice_tableMouseClicked
        int rowindex = invoice_table.getSelectedRow();
        selectedid = (int) invoice_table.getModel().getValueAt(rowindex, 0);
        
        try {
            String getrowdata = "SELECT * FROM payment WHERE Invoice_ID="+selectedid;
            pst = (PreparedStatement)conn.prepareStatement(getrowdata);
            rs = pst.executeQuery();
            
            if(rs.next()==true){
                studentid.setText(rs.getString("Student_ID"));
                courseid.setText(rs.getString("Course_ID"));
                batch.setText(rs.getString("Batch"));
                nic.setText(rs.getString("NIC_NO"));
                studentname.setText(rs.getString("Student_Name"));
                studentemail.setText(rs.getString("Student_Email"));
                paymenttype.setSelectedItem(rs.getString("Payment_Type"));
                paymentamount.setText(rs.getString("Payment_Amount"));
                
            }
        
        } catch (SQLException ex) {
            Logger.getLogger(Student_records.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }//GEN-LAST:event_invoice_tableMouseClicked

