    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            String getstudentid = studentid.getText();
            String getcourseid = courseid.getText();
            String getnic = nic.getText();
            String getbatch = batch.getText();
            String getstudentname = studentname.getText();
            String getstudentemail = studentemail.getText();
            String getpaymenttype = (String) paymenttype.getSelectedItem();
            String getpaymentamount = paymentamount.getText();
            
            String update = "UPDATE payment SET Student_ID='" + getstudentid + "', Course_ID='" + getcourseid + "', NIC_NO='" + getnic + "',Batch='" + getbatch + "', Student_Name='" + getstudentname + "', Student_Email='" + getstudentemail + "', Payment_Type='" + getpaymenttype + "', Payment_Amount='" + getpaymentamount + "' WHERE Invoice_ID = '"+selectedid+"'";
            pst = (PreparedStatement) conn.prepareStatement(update);
            pst.execute();
            table();
            JOptionPane.showMessageDialog(null, "Record Successfully Updated..");
            
        } catch (SQLException ex) {
            Logger.getLogger(Student_records.class.getName()).log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String message = "Are you sure you want to delete this record???";
        int reply = JOptionPane.showConfirmDialog(null, message, "Delete Record",JOptionPane.YES_NO_OPTION);
        
        if(reply == JOptionPane.YES_OPTION){
            try {
                String deleteqery ="DELETE FROM payment Where Invoice_ID=?";
                pst = (PreparedStatement) conn.prepareStatement(deleteqery);
                pst.setInt(1, selectedid);
                pst.executeUpdate();
                table();
            } catch (SQLException ex) {
                Logger.getLogger(Student_records.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

