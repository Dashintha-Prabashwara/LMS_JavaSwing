    private void searchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchKeyReleased
        try {
            String userinput = search.getText();
            conn =  EvoDatabase.connect();
            String getdata = "SELECT Invoice_ID,Student_ID,Student_Name,NIC_NO,Course_ID,Payment_Type FROM payment WHERE Invoice_ID LIKE '%"+userinput+"%' OR Student_ID LIKE '%"+userinput+"%' OR Student_Name LIKE '%"+userinput+"%' OR Course_ID LIKE '%"+userinput+"%' OR NIC_NO LIKE '%"+userinput+"%' OR Payment_Type LIKE '%"+userinput+"%' OR Batch LIKE '%"+userinput+"%'";
            pst = (PreparedStatement)conn.prepareStatement(getdata);
            ResultSet search_rs = pst.executeQuery();
            invoice_table.setModel(DbUtils.resultSetToTableModel(search_rs));

        } catch (SQLException ex) {
            Logger.getLogger(Invoice_records.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_searchKeyReleased

